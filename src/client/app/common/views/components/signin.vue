<template>
<form class="mk-signin" :class="{ signing, totpLogin }" @submit.prevent="onSubmit">
	<div v-show="withAvatar" class="avatar" :style="{ backgroundImage: user ? `url('${ user.avatarUrl }')` : null }"></div>
	<div v-if="!totpLogin" class="normal-signin">
		<ui-input v-model="username" type="text" pattern="^[a-zA-Z0-9_]+$" spellcheck="false" autofocus required @input="onUsernameChange">
			<span>{{ $t('username') }}</span>
			<template #prefix>@</template>
			<template #suffix>@{{ host }}</template>
		</ui-input>
		<ui-input v-if="!user || user && !user.usePasswordLessLogin" v-model="password" type="password" :with-password-toggle="true" required>
			<span>{{ $t('password') }}</span>
			<template #prefix><fa icon="lock"/></template>
		</ui-input>
		<ui-button type="submit" :disabled="signing">{{ signing ? $t('signing-in') : $t('@.signin') }}</ui-button>
		<p style="margin: 8px 0;"><a @click="onReminder">{{ $t('@.reminder') }}</a></p>
		<p v-if="meta && meta.enableTwitterIntegration" style="margin: 8px 0;"><a :href="`${apiUrl}/signin/twitter`"><fa :icon="['fab', 'twitter']"/> {{ $t('signin-with-twitter') }}</a></p>
		<p v-if="meta && meta.enableGithubIntegration"  style="margin: 8px 0;"><a :href="`${apiUrl}/signin/github`"><fa :icon="['fab', 'github']"/> {{ $t('signin-with-github') }}</a></p>
		<p v-if="meta && meta.enableDiscordIntegration" style="margin: 8px 0;"><a :href="`${apiUrl}/signin/discord`"><fa :icon="['fab', 'discord']"/> {{ $t('signin-with-discord') /* TODO: Make these layouts better */ }}</a></p>
		<p style="margin: 8px 0;"><a @click="onFlush">{{ $t('@.flush') }}</a></p>
	</div>
	<div v-if="totpLogin" class="2fa-signin" :class="{ securityKeys: user && user.securityKeys }">
		<div v-if="user && user.securityKeys" class="twofa-group tap-group">
			<p>{{ $t('tap-key') }}</p>
			<ui-button v-if="!queryingKey" @click="queryKey">
				{{ $t('@.error.retry') }}
			</ui-button>
		</div>
		<div v-if="user && user.securityKeys" class="or-hr">
			<p class="or-msg">{{ $t('or') }}</p>
		</div>
		<div class="twofa-group totp-group">
			<p style="margin-bottom:0;">{{ $t('enter-2fa-code') }}</p>
			<ui-input v-if="user && user.usePasswordLessLogin" v-model="password" type="password" :with-password-toggle="true" required>
				<span>{{ $t('password') }}</span>
				<template #prefix><fa icon="lock"/></template>
			</ui-input>
			<ui-input v-model="token" type="text" pattern="^[0-9]{6}$" autocomplete="off" spellcheck="false" required>
				<span>{{ $t('@.2fa') }}</span>
				<template #prefix><fa icon="gavel"/></template>
			</ui-input>
			<ui-button type="submit" :disabled="signing">{{ signing ? $t('signing-in') : $t('@.signin') }}</ui-button>
		</div>
	</div>
</form>
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import i18n from '../../../i18n';
import { apiUrl, host } from '../../../config';
import { toUnicode } from 'punycode';
import { hexifyAB, byteify } from '../../scripts/2fa';
import { ayuskeyApi } from '../../../api';
import { assertIsSuccess } from 'strictcat/built/rpc'

export default defineComponent({
	i18n: i18n('common/views/components/signin.vue'),

	props: {
		withAvatar: {
			type: Boolean,
			required: false,
			default: true,
		},
	},

	data() {
		return {
			signing: false,
			user: null,
			username: '',
			password: '',
			token: '',
			apiUrl,
			host: toUnicode(host),
			meta: null,
			totpLogin: false,
			credential: null,
			challengeData: null,
			queryingKey: false,
		};
	},

	created() {
		this.$root.getMeta().then(meta => {
			this.meta = meta;
		});
	},

	methods: {
		onUsernameChange() {
			ayuskeyApi.call('POST', '/users/show', {
				body: {
					username: this.username,
				}
			}).then(user => {
				assertIsSuccess(user)
				this.user = user.data;
			}, () => {
				this.user = null;
			});
		},

		queryKey() {
			this.queryingKey = true;
			return navigator.credentials.get({
				publicKey: {
					challenge: byteify(this.challengeData.challenge, 'base64'),
					allowCredentials: this.challengeData.securityKeys.map(key => ({
						id: byteify(key.id, 'hex'),
						type: 'public-key',
						transports: ['usb', 'nfc', 'ble', 'internal'],
					})),
					timeout: 60 * 1000,
				},
			}).catch(() => {
				this.queryingKey = false;
				return Promise.reject(null);
			}).then(credential => {
				this.queryingKey = false;
				this.signing = true;
				return this.$root.api('signin', {
					username: this.username,
					password: this.password,
					signature: hexifyAB(credential.response.signature),
					authenticatorData: hexifyAB(credential.response.authenticatorData),
					clientDataJSON: hexifyAB(credential.response.clientDataJSON),
					credentialId: credential.id,
					challengeId: this.challengeData.challengeId,
				});
			}).then(res => {
				localStorage.setItem('i', res.i);
				document.cookie = `token=${res.i}; path=/; max-age=31536000`; // bull dashboardの認証とかで使う
				location.reload();
			}).catch(err => {
				if (err === null) return;
				this.$root.dialog({
					type: 'error',
					text: this.$t('login-failed'),
				});
				this.signing = false;
			});
		},

		onReminder() {
					this.$emit('reminder');
				},

		onSubmit() {
			this.signing = true;

			if (!this.totpLogin && this.user && this.user.twoFactorEnabled) {
				if (window.PublicKeyCredential && this.user.securityKeys) {
					this.$root.api('signin', {
						username: this.username,
						password: this.password,
					}).then(res => {
						this.totpLogin = true;
						this.signing = false;
						this.challengeData = res;
						return this.queryKey();
					}).catch(() => {
						this.$root.dialog({
							type: 'error',
							text: this.$t('login-failed'),
						});
						this.challengeData = null;
						this.totpLogin = false;
						this.signing = false;
					});
				} else {
					this.totpLogin = true;
					this.signing = false;
				}
			} else {
				this.$root.api('signin', {
					username: this.username,
					password: this.password,
					token: this.user && this.user.twoFactorEnabled ? this.token : undefined,
				}).then(res => {
					localStorage.setItem('i', res.i);
					document.cookie = `token=${res.i}; path=/; max-age=31536000`; // bull dashboardの認証とかで使う
					location.reload();
				}).catch(() => {
					this.$root.dialog({
						type: 'error',
						text: this.$t('login-failed'),
					});
					this.signing = false;
				});
			}
		},

		onFlush() {
			const r = confirm('ブラウザに保存されたキャッシュをクリアしますか？');
			if (r) location.href = '/flush';
		},
	},
});
</script>

